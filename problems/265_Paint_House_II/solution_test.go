package _65_Paint_House_II

import "testing"

func TestMinCostII(t *testing.T) {
	testcases := []struct {
		costs  [][]int
		except int
	}{
		{
			[][]int{{17, 2, 17}, {16, 16, 5}, {14, 3, 19}},
			10,
		},
		{
			[][]int{{7, 6, 2}},
			2,
		},
		{
			[][]int{{8, 16, 12, 18, 9}, {19, 18, 8, 2, 8}, {8, 5, 5, 13, 4}, {15, 9, 3, 19, 2}, {8, 7, 1, 8, 17}, {8, 2, 8, 15, 5}, {8, 17, 1, 15, 3}, {8, 8, 5, 5, 16}, {2, 2, 18, 2, 9}},
			28,
		},
		{
			[][]int{{7, 19, 11, 3, 7, 15, 17, 5, 6, 18, 1, 15, 18, 11}, {13, 18, 18, 8, 13, 12, 11, 13, 4, 8, 2, 4, 5, 20}, {14, 5, 18, 4, 7, 6, 1, 6, 11, 6, 16, 6, 13, 17}, {18, 17, 11, 3, 12, 4, 8, 6, 2, 7, 10, 9, 19, 3}, {4, 3, 2, 14, 11, 15, 18, 1, 17, 1, 6, 14, 14, 9}, {9, 13, 15, 14, 5, 1, 1, 6, 11, 15, 16, 12, 10, 18}, {19, 2, 11, 3, 13, 4, 13, 7, 16, 16, 20, 18, 20, 8}, {8, 19, 20, 9, 18, 13, 17, 1, 2, 4, 3, 20, 15, 9}, {9, 10, 11, 6, 14, 20, 4, 1, 5, 15, 13, 10, 13, 5}, {13, 11, 9, 11, 9, 16, 3, 19, 1, 11, 6, 7, 12, 13}, {14, 1, 15, 14, 11, 12, 7, 14, 12, 11, 6, 9, 5, 5}},
			17,
		},
	}
	for _, testcase := range testcases {
		actual := minCostII(testcase.costs)
		if actual != testcase.except {
			t.Errorf("costs: %v, except: %v, actual: %v", testcase.costs, testcase.except, actual)
		}
	}

}
